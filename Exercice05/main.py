def sum(a, b):
    """
    Additionne deux nombres et retourne le résultat.

    Paramètres:
    a (int ou float): Le premier nombre.
    b (int ou float): Le deuxième nombre.

    Retours:
    int ou float: La somme des deux nombres.
    """
    return a + b


def subtraction(a, b):
    """
    Soustrait deux nombres et retourne le résultat.

    Paramètres:
    a (int ou float): Le premier nombre.
    b (int ou float): Le deuxième nombre.

    Retours:
    int ou float: La différence des deux nombres.
    """
    return a - b
